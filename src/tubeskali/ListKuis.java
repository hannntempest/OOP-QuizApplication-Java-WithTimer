    private void jButton5MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton5MouseClicked
        String kuis = jList1.getSelectedValue();
        String s = jTextField1.getText();
        String n = jTextField2.getText();
        
        if(kuis != null){
            if(!s.isEmpty() && !n.isEmpty()){
                try {
                    Connection con = DriverManager.getConnection(DB_URL, DB_USER, DB_PASS);
                    String query = "UPDATE quizzes SET subject = ?, name = ? WHERE name = ?";
                    PreparedStatement ps = con.prepareStatement(query);
                    ps.setString(1, s);
                    ps.setString(2, n);
                    ps.setString(3, kuis);
                    
                    int affectedRow = ps.executeUpdate();
                    compileKuis();
                    
                    ps.close();
                    con.close();
                    
                    if(affectedRow > 0){
                        JOptionPane.showMessageDialog(null, "Kuis Berhasil Di Update", "Success", JOptionPane.INFORMATION_MESSAGE);
                    }else{
                        JOptionPane.showMessageDialog(null, "Gagal Update Kuis", "Error", JOptionPane.ERROR_MESSAGE);                        
                    }
                }catch (SQLException e){
                    Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, e);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Isi Semua Data! ", "Error", JOptionPane.ERROR_MESSAGE);                
            }
        } else {
           JOptionPane.showMessageDialog(null, "Pilih Kuis terlebih dahulu! ", "Error", JOptionPane.ERROR_MESSAGE);                            
        }
    }//GEN-LAST:event_jButton5MouseClicked

    private void jButton4MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton4MouseClicked
        String kuis = jList2.getSelectedValue();
        String s = jTextField1.getText();
        String n = jTextField2.getText();
        
        if(kuis != null){
            try {
                Connection con = DriverManager.getConnection(DB_URL, DB_USER, DB_PASS);
                PreparedStatement ps = con.prepareStatement("SELECT * FROM quizzes JOIN questions ON quizzes.question_id = questions.question_id WHERE questions.question=?");
                PreparedStatement ps2 = con.prepareStatement("DELETE FROM quizzes WHERE subject = ? AND name = ? AND question_id = ?");
                ps.setString(1, kuis);
                    
                int affectedRow = 0;
                
                ResultSet rs = ps.executeQuery();
                if(rs.next()){
                    String id = rs.getString("question_id");
                    ps2.setString(1, s);
                    ps2.setString(2, n);
                    ps2.setString(3, id);
                    
                    affectedRow = ps2.executeUpdate();
                }
                
                compileKuis();
                    
                ps.close();
                con.close();
                
                dispose();
                ListKuis lk = new ListKuis(user);
                lk.setTitle("List Kuis");
                lk.setLocationRelativeTo(null); // center a form.
                lk.setVisible(true);
                    
                if(affectedRow > 0){
                    JOptionPane.showMessageDialog(null, "Data Berhasil Dihapus", "Success", JOptionPane.INFORMATION_MESSAGE);
                }else{
                    JOptionPane.showMessageDialog(null, "Gagal Hapus Data", "Error", JOptionPane.ERROR_MESSAGE);                        
                }
            }catch (SQLException e){
                Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, e);
            }
        } else {
           JOptionPane.showMessageDialog(null, "Pilih Soal Terlebih Dahulu! ", "Error", JOptionPane.ERROR_MESSAGE);                            
        }
    }//GEN-LAST:event_jButton4MouseClicked

