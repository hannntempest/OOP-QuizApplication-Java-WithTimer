    private void compileKuis(){
        try {
            Connection con = DriverManager.getConnection(DB_URL, DB_USER, DB_PASS);
            PreparedStatement ps = con.prepareStatement("SELECT name FROM quizzes");
            ResultSet res = ps.executeQuery();
            
            poolKuis = new DefaultListModel<>();
            
            while(res.next()){
                boolean unique = true;
                String soal = res.getString("name");
                int el = poolKuis.getSize();
                int i = 0;
                while(i < el && unique){
                    if(poolKuis.getElementAt(i).equals(soal)){
                        unique = false;
                    }
                    i++;
                }
                if(unique){
                    poolKuis.addElement(soal);
                }
            }
            
            jList1.setModel(poolKuis);
            res.close();
            ps.close();
            con.close();
        }catch(SQLException e){
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, e);
        }
    }
    
    private void detailQuiz(String name){
        try {
            Connection con = DriverManager.getConnection(DB_URL, DB_USER, DB_PASS);
            String query = "SELECT * FROM quizzes WHERE name=?";
            PreparedStatement ps = con.prepareStatement(query);
            ps.setString(1, name);
            ResultSet res = ps.executeQuery();
            
            poolSoal = new DefaultListModel<>();
            
            if(res.next()){
                jTextField1.setText(res.getString("subject"));
                jTextField2.setText(res.getString("name"));
                PreparedStatement ps2 = con.prepareStatement("SELECT * FROM quizzes JOIN questions ON quizzes.question_id = questions.question_id WHERE quizzes.name=? AND quizzes.subject=?");
                
                ps2.setString(1, jTextField2.getText());
                ps2.setString(2, jTextField1.getText());
                
                ResultSet rs = ps2.executeQuery();
                
                while(rs.next()){
                    String soal = rs.getString("question");
                    poolSoal.addElement(soal);
                }
                
                jList2.setModel(poolSoal);
                rs.close();
                ps2.close();
            }
            
            res.close();
            ps.close();
            con.close();
        }catch (SQLException e){
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, e);
        }
    }

