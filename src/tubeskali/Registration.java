    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        String name = jTextField1.getText();
        String username = jTextField2.getText();
        String telp = jTextField3.getText();
        String email = jTextField4.getText();
        String pw = jTextField5.getText();
        String gender = jRadioButton1.isSelected() ? "Laki-Laki" : "Perempuan";
        
        if(name.isEmpty() || username.isEmpty() || telp.isEmpty() || email.isEmpty() || pw.isEmpty() || gender.isEmpty()){
            JOptionPane.showMessageDialog(this, "Data Belum Terisi Seluruhnya!", "Error", JOptionPane.ERROR_MESSAGE);
        }else if(email.startsWith("student") || email.startsWith("admin") || email.startsWith("lecturer")) {
            JOptionPane.showMessageDialog(this, "Email Tidak Valid!", "Error", JOptionPane.ERROR_MESSAGE);
        }else{
            if(email.contains("@student")){
                studentRegister(name, username, telp, email, pw, gender);
            }else if(email.contains("@admin")){
                adminRegister(name, username, telp, email, pw, gender);
            }else if(email.contains("@lecturer")){
                lectureRegister(name, username, telp, email, pw, gender);
            }else{
                JOptionPane.showMessageDialog(this, "Email Domain Tidak Valid!", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton1MouseClicked

