    private void studentRegister(String name, String username, String telp, String email, String pw, String gender){
        try {
            Connection conn = DriverManager.getConnection(DB_URL, DB_USER, DB_PASS);
            PreparedStatement ps = conn.prepareStatement("INSERT INTO student(name,username,phone_number,email,password,gender)VALUES(?,?,?,?,?,?)");
            
            ps.setString(1, name);
            ps.setString(2, username);
            ps.setString(3, telp);
            ps.setString(4, email);
            ps.setString(5, pw);
            ps.setString(6, gender);
            
            int row = ps.executeUpdate();
            
            ps.close();
            conn.close();
            
            if(row > 0){
                JOptionPane.showMessageDialog(this, "Registrasi Mahasiswa Berhasil", "Success", JOptionPane.INFORMATION_MESSAGE);
            }else{
                JOptionPane.showMessageDialog(null, "Gagal Registrasi", "Error", JOptionPane.ERROR_MESSAGE);                        
            }
            
            dispose();
            Login l = new Login();
            l.setTitle("Login Page");
            l.setLocationRelativeTo(null); // center the form.
            l.setVisible(true);
            
        } catch(SQLException e){
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, e);
        }
    }
    
