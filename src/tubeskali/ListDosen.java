    public ListDosen(String user) {
        initComponents();
        this.user = user;
        listDosen = new DefaultListModel();
        jList1.setModel(listDosen);
        compileDosen();
        jList1.addListSelectionListener(new ListSelectionListener() {
            @Override
            public void valueChanged(ListSelectionEvent e){
                if(!e.getValueIsAdjusting()){
                    String user = jList1.getSelectedValue();
                    if(user != null){
                        dataDosen(user);
                    }
                }
            }
        });
    }
    
    private void compileDosen(){
        try {
            Connection con = DriverManager.getConnection(DB_URL, DB_USER, DB_PASS);
            PreparedStatement ps = con.prepareStatement("SELECT name FROM lecturer");
            ResultSet res = ps.executeQuery();
            
            listDosen = new DefaultListModel<>();
            
            while(res.next()){
                String user = res.getString("name");
                listDosen.addElement(user);
            }
            
            jList1.setModel(listDosen);
            res.close();
            ps.close();
            con.close();
        }catch(SQLException e){
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, e);
        }
    }
    
    private void dataDosen(String user){
        try {
            Connection con = DriverManager.getConnection(DB_URL, DB_USER, DB_PASS);
            String query = "SELECT * FROM lecturer WHERE name=?";
            PreparedStatement ps = con.prepareStatement(query);
            ps.setString(1, user);
            ResultSet res = ps.executeQuery();
            
            if(res.next()){
                jLabel6.setText(res.getString("lecturer_id"));
                jLabel7.setText(res.getString("name"));
                jLabel8.setText(res.getString("username"));
                jLabel9.setText(res.getString("email"));
                jTextField1.setText(res.getString("subject"));
            }
            
            res.close();
            ps.close();
            con.close();
        }catch (SQLException e){
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, e);
        }
    }

