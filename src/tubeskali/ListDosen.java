    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked
        String name = jList1.getSelectedValue();
        String s = jTextField1.getText();
        
        if(name != null){
            if(!s.isEmpty()){
                try {
                    Connection con = DriverManager.getConnection(DB_URL, DB_USER, DB_PASS);
                    String query = "UPDATE lecturer SET subject = ? WHERE name = ?";
                    PreparedStatement ps = con.prepareStatement(query);
                    ps.setString(1, s);
                    ps.setString(2, name);
                    
                    int affectedRow = ps.executeUpdate();
                    compileDosen();
                    
                    ps.close();
                    con.close();
                    
                    if(affectedRow > 0){
                        JOptionPane.showMessageDialog(null, "Data Dosen Berhasil Di Update", "Success", JOptionPane.INFORMATION_MESSAGE);
                    }else{
                        JOptionPane.showMessageDialog(null, "Gagal Update Data Dosen", "Error", JOptionPane.ERROR_MESSAGE);                        
                    }
                }catch (SQLException e){
                    Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, e);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Isi Semua Data! ", "Error", JOptionPane.ERROR_MESSAGE);                
            }
        } else {
           JOptionPane.showMessageDialog(null, "Pilih Dosen terlebih dahulu! ", "Error", JOptionPane.ERROR_MESSAGE);                            
        }
    }//GEN-LAST:event_jButton2MouseClicked

